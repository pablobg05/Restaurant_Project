    private void cargarTabla(){
        try {
            List<Receta> lista = controller.getRecetas();
            
            for(int filas = 0; filas < lista.size(); filas++){
                Tabla.setValueAt(lista.get(filas).getId_receta(), filas, 0);
                Tabla.setValueAt(lista.get(filas).getId_producto(), filas, 1);
                Tabla.setValueAt(lista.get(filas).getCantidad(), filas, 2);
                Tabla.setValueAt(lista.get(filas).getCategoria(), filas, 3);
                
               
                
            }
            
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        
        
    }

